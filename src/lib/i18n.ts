export type AppLocale = keyof typeof messages;

function detectLocale(): AppLocale {
  const locale = globalThis.navigator?.language?.toLowerCase() ?? 'en';
  return locale.startsWith('zh') ? 'zh' : 'en';
}

export async function getStoredLocale(): Promise<AppLocale | null> {
  const stored = await browser.storage.local.get(LOCALE_STORAGE_KEY);
  const value = stored[LOCALE_STORAGE_KEY];
  return value === 'zh' || value === 'en' ? value : null;
}

export async function saveStoredLocale(locale: AppLocale): Promise<void> {
  await browser.storage.local.set({
    [LOCALE_STORAGE_KEY]: locale,
  });
}

export function createAppI18n() {
  return createI18n({
    legacy: false,
    locale: detectLocale(),
    fallbackLocale: 'en',
    messages,
  });
}
